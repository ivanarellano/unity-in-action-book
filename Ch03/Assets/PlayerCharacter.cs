using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class PlayerCharacter : MonoBehaviour {

    private int health;

	void Start () {
        health = 5;
	}
	
	void Update () {
		
	}

    public void Hurt(int damage) {
        health -= damage;
        Debug.Log("Health: " + health);
    }
}
